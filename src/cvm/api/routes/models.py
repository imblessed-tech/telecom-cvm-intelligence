@router.get("/alerts", response_model=List[Dict[str, Any]])
def get_models_alerts():
    """Retrieve the log of all active warnings and critical alerts generated by the monitor daemon."""
    path = settings.MONITORING_DIR / "alerts_log.json"
    
    if not path.exists():
        return []
        
    try:
        with open(path, "r") as f:
            alerts = json.load(f)
        return alerts
    except Exception as e:
        logger.error(f"Error reading alert logs from {path}: {e}", exc_info=True)
        raise HTTPException(status_code=500, detail="Error reading active CVM alerts.")
